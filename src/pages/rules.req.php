<h2>Allowed content</h2>
<ul>
	<li>Only content from Asia is permitted. Anything else will be removed on sight and you may be warned or banned.</li>
	<li>Hentai is permitted, provided that they are tagged and categorised correctly.</li>
</ul>

<h2>Banned content</h2>
<ul>
	<li>Do not upload scans of official releases.</li>
	<li>Do not upload raws.</li>
	<li>"Troll" content are not allowed. These will be removed on sight.</li>
</ul>

<h2>Content control</h2>
<ul>
	<li>Generally speaking, the Group Leader has the final say (editing/removal) over their content. </li>
	<li>For content which does not have a Group Leader (because the group is dead or no one has claimed leadership), the uploader has the final say.</li>
	<li>If there are disputes between groups (Group A wants Group B's content to be removed), mods will examine the reasons, common sense will apply.</li>
	<li>If a group does not want their content hosted here, it is their responsibility to take over their group and to police the content attributed to them.</li>
</ul>

<h2>Commenting guidelines</h2>
